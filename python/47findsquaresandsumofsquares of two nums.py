# program to get 2 numbers from user, find
# the cube, add the squares and display the result.

import math

num=[]
x = int(input("Enter the first value: "))
y = int(input("Enter the first value: "))
sq_x = math.pow(x, 3)
sq_y = math.pow(y, 3)
num.append(sq_x)
num.append(sq_y)
total = sum(num)

print(f"The cubes of {x} and {y} are: {sq_x}, {sq_y} and the sum of their cubes is {total}")